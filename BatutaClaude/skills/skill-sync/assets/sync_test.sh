# --- Extraction tests ---

test_extract_field_string() {
    setup_mock_repo
    local skill_file="$MOCK_DIR/BatutaClaude/skills/test-skill-a/SKILL.md"

    # Source the extract functions (they're in sync.sh)
    # We'll use bash to call them
    local name
    name=$(bash -c "
        $(declare -f 2>/dev/null || true)
        source '$SYNC_SCRIPT' 2>/dev/null || true
        extract_field '$skill_file' 'name'
    " 2>/dev/null) || name=""

    # Fallback: extract directly with awk
    if [[ -z "$name" ]]; then
        name=$(awk '
            /^---$/ { in_front++; next }
            in_front == 1 && /^name:/ {
                val = $0; sub(/^name:[ ]*/, "", val)
                gsub(/^["'"'"'"]|["'"'"'"]$/, "", val)
                gsub(/^[ \t]+|[ \t]+$/, "", val)
                print val; exit
            }
            in_front >= 2 { exit }
        ' "$skill_file")
    fi

    assert_eq "test-skill-a" "$name" "Should extract name field"
    teardown_mock_repo
}

test_extract_metadata_string() {
    setup_mock_repo
    local skill_file="$MOCK_DIR/BatutaClaude/skills/test-skill-a/SKILL.md"

    local scope
    scope=$(awk '
        /^---$/ { in_front++; next }
        in_front == 1 {
            if (/^metadata:/) { in_meta = 1; next }
            if (in_meta && /^[^ ]/) { in_meta = 0 }
            if (in_meta && /^  scope:/) {
                val = $0; sub(/^  scope:[ ]*/, "", val)
                gsub(/^[ \t]+|[ \t]+$/, "", val)
                print val; exit
            }
        }
        in_front >= 2 { exit }
    ' "$skill_file")

    assert_eq "[pipeline]" "$scope" "Should extract metadata.scope"
    teardown_mock_repo
}

test_extract_metadata_list() {
    setup_mock_repo
    local skill_file="$MOCK_DIR/BatutaClaude/skills/test-skill-c/SKILL.md"

    local auto_invoke
    auto_invoke=$(awk '
        /^---$/ { in_front++; next }
        in_front == 1 {
            if (/^metadata:/) { in_meta = 1; next }
            if (in_meta && /^[^ ]/) { in_meta = 0 }
            if (in_meta && /^  auto_invoke:/) {
                val = $0; sub(/^  auto_invoke:[ ]*/, "", val)
                gsub(/^[ \t]+|[ \t]+$/, "", val)
                if (val == "" || val == ">") {
                    list_items = ""
                    while (getline > 0) {
                        if (/^    - /) {
                            item = $0; sub(/^    - ["'"'"'"'"'"'"]?/, "", item)
                            sub(/["'"'"'"'"'"'"]?$/, "", item)
                            gsub(/^[ \t]+|[ \t]+$/, "", item)
                            if (list_items != "") list_items = list_items "|"
                            list_items = list_items item
                        } else { break }
                    }
                    if (list_items != "") print list_items
                } else {
                    print val
                }
                exit
            }
        }
        in_front >= 2 { exit }
    ' "$skill_file")

    assert_contains "$auto_invoke" "Tracking events" "Should contain first list item"
    assert_contains "$auto_invoke" "Analyzing patterns" "Should contain second list item"
    teardown_mock_repo
}

